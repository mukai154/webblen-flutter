class CustomDates {

  weekdayToString(int day){
    if (day == 1){
      return "Sunday";
    } else if (day == 2){
      return "Monday";
    } else if (day == 3){
      return "Tuesday";
    } else if (day == 4){
      return "Wednesday";
    } else if (day == 5){
      return "Thursday";
    } else if (day == 6){
      return "Friday";
    } else if (day == 7){
      return "Saturday";
    }
  }

  monthToString(int day){
    if (day == 1){
      return "Jan";
    } else if (day == 2){
      return "Feb";
    } else if (day == 3){
      return "Mar";
    } else if (day == 4){
      return "Apr";
    } else if (day == 5){
      return "May";
    } else if (day == 6){
      return "Jun";
    } else if (day == 7){
      return "Jul";
    } else if (day == 8){
      return "Aug";
    } else if (day == 9){
      return "Sep";
    } else if (day == 10){
      return "Oct";
    } else if (day == 11){
      return "Nov";
    } else if (day == 12){
      return "Dec";
    }
  }

}